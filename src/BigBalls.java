public class BigBalls {
    private ColorBalls color;
    private ParametrsBalls parametrs;

    public BigBalls(ColorBalls color, ParametrsBalls parametrs) {
        this.color = color;
        this.parametrs = parametrs;
    }

    public ColorBalls getColor() {
        return color;
    }

    public ParametrsBalls getParametrs() {
        return parametrs;
    }
}